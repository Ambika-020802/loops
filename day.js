
//FOR LOOP

for(var a=1;a<3;a++){
    console.log(a);
    
}


for (var a=10;a<=15;a++){
    console.log(a);
}


for(var a=10;a>7;a--){
    console.log(a);
    
}


for(var i=20;i>=15;i--){
    console.log(i);
    
}




